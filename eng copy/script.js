// Генерирует заданное количество рандомных слов из массива
function generateRandomWords(numWords) {
  const randomWords = [];
  for (let i = 0; i < numWords; i++) {
    const randomIndex = Math.floor(Math.random() * words.length);
    randomWords.push(words[randomIndex]);
  }
  return randomWords;
}

// Создает инпут для ввода предложения с каждым словом
function createWordInputs() {
  const wordsContainer = document.getElementById("words-container");
  const randomWords = generateRandomWords(15);
  
  randomWords.forEach(word => {
    const wordInputDiv = document.createElement("div");
    wordInputDiv.style.marginBottom = "10px";

    const wordSpan = document.createElement("span");
    wordSpan.textContent = `Слово: ${word}`;
    wordInputDiv.appendChild(wordSpan);

    const wordInput = document.createElement("input");
    wordInput.type = "text";
    wordInput.placeholder = `Введите предложение с словом "${word}"`;
    wordInput.addEventListener("change", checkWordInput);
    wordInputDiv.appendChild(wordInput);
    wordInput.style.minWidth = '300px';
    wordsContainer.appendChild(wordInputDiv);
  });
}

// Проверяет, правильно ли введено слово
function checkWordInput(event) {
  const input = event.target;
  const word = input.placeholder.split('"')[1];
  
  if (input.value.toLowerCase().includes(word)) {
    input.style.backgroundColor = "lightgreen";
    input.setAttribute("readonly", true);
  } else {
    input.style.backgroundColor = "pink";
  }
}


